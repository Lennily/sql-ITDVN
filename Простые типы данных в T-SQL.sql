-- Создаем переменную с именем "а", типа Int, присваиваем ей значение - 2ю
DECLARE @A INT = 2;

-- Выводим на экран значение переменной @A.
PRINT @A;  --2

-- Переменной "а" присваиваем значение - 7
SET @A = 7;

-- Выводим на экран значение переменной "Фю
PRINT @A; --7